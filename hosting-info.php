Wat is hosting?<br><br>

Met hosting wordt bedoeld het verhuren van ruimte op internet voor het opslaan van informatie, <br>
afbeeldingen, video’s of andere data. <br><br>

Het Engelse woord “host” betekent letterlijk “gastheer”. <br>
Een bedrijf dat hosting aanbiedt is in feite gastheer van uw website.<br> 
Dit bedrijf zorgt ervoor dat uw website via het internet toegankelijk is. <br>
Uw website wordt op een speciale computer geplaatst. Een server genaamd. <br>
Deze server wordt door het hostingbedrijf op technisch niveau onderhouden en heeft een door het bedrijf verzorgde verbinding met internet. <br>


      
